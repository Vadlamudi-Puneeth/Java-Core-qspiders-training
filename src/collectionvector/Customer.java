package collectionvector;

public class Customer {
		int id;
		String name;
		long phno;
		
		Customer(int id, String name, long phno){
			this.id = id;
			this.name = name;
			this.phno = phno;
		}
		
		public int getId() {
			return id;
		}
		
		public String getName() {
			return name;
		}
		
		public long getNo() {
			return phno;
		}
		
	
}
